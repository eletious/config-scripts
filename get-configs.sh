#! /bin/bash

## collect scripts

# collect wallpaper
cp ~/Pictures/wallpaper.jpg ~/config-scripts/images/wallpaper.jpg

# tmux
cp ~/.tmux.conf ~/config-scripts/dotfiles/.tmux.conf

# i3
cp ~/.config/i3/config ~/config-scripts/dotfiles/i3-config
cp ~/.i3status.conf	~/config-scripts/dotfiles/i3status.conf

# vim
cp ~/.vimrc ~/config-scripts/dotfiles/vimrc

# weechat
cp ~/.weechat/weechat.conf ~/config-scripts/dotfiles/weechat.conf
